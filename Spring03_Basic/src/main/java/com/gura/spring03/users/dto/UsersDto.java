package com.gura.spring03.users.dto;

public class UsersDto {
	private String id;
	private String pwd;
	
	public UsersDto() {}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
}
